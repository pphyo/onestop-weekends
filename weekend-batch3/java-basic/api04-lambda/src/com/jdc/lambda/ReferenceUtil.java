package com.jdc.lambda;

public class ReferenceUtil {
	
	public ReferenceUtil() {}
	
	public ReferenceUtil(String data) {
		System.out.println(data);
	}
	
	public ReferenceUtil(int data) {}
	
	public static int takeAway(int a, int b) {
		return a - b;
	}
	
	public void display(String data) {
		System.out.println(data);
	}

}
